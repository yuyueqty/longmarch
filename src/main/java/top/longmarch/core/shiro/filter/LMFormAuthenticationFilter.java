package top.longmarch.core.shiro.filter;

import org.apache.shiro.web.filter.authc.FormAuthenticationFilter;
import org.apache.shiro.web.util.WebUtils;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import top.longmarch.core.utils.ResponseUtil;

import javax.servlet.ServletRequest;
import javax.servlet.ServletResponse;


public class LMFormAuthenticationFilter extends FormAuthenticationFilter {

    private static final Logger log =
            LoggerFactory.getLogger(LMFormAuthenticationFilter.class);

    @Override
    protected boolean onAccessDenied(ServletRequest request, ServletResponse response) throws Exception {
        if (isLoginRequest(request, response)) {
            if (isLoginSubmission(request, response)) {
                if (log.isTraceEnabled()) {
                    log.trace("Login submission detected.  Attempting to execute login.");
                }
                return executeLogin(request, response);
            } else {
                if (log.isTraceEnabled()) {
                    log.trace("Login page view.");
                }
                return true;
            }
        } else {
            if (log.isTraceEnabled()) {
                log.trace("Attempting to access a path which requires authentication.  Forwarding to the " +
                        "Authentication url [" + getLoginUrl() + "]");
            }
            saveRequest(request);
            ResponseUtil.respondJson(WebUtils.toHttp(response), 10, "用户未登陆");
            return false;
        }
    }

}
